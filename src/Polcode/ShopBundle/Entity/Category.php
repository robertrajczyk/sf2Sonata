<?php

namespace Polcode\ShopBundle\Entity;

use Doctrine\Common\Collections\ArrayCollection;

class Category
{
    protected $name;

    protected $products;

    public function __construct()
    {
        $this->products = new ArrayCollection();
    }
    /**
     * @var integer
     */
    private $id;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Category
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Add products
     *
     * @param \Polcode\ShopBundle\Entity\Product $products
     * @return Category
     */
    public function addProduct(\Polcode\ShopBundle\Entity\Product $products)
    {
        $this->products[] = $products;

        return $this;
    }

    /**
     * Remove products
     *
     * @param \Polcode\ShopBundle\Entity\Product $products
     */
    public function removeProduct(\Polcode\ShopBundle\Entity\Product $products)
    {
        $this->products->removeElement($products);
    }

    /**
     * Get products
     *
     * @return \Doctrine\Common\Collections\Collection 
     */
    public function getProducts()
    {
        return $this->products;
    }

    public function __toString()
    {
        return $this->getName();
    }
}
